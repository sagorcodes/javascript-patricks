// Declare two number type variables
var num1 = 10;
var num2 = 20;

// Comparisons
var isLessThan = num1 < num2;
var isGreaterThan = num1 > num2;
var isEqualTo = num1 == num2;
var isNotEqual = num1 != num2;
var isLessThanOrEqualTo = num1 <= num2;
var isGreaterThanOrEqualTo = num1 >= num2;

// Output the results
console.log("Is num1 less than num2?", isLessThan);
console.log("Is num1 greater than num2?", isGreaterThan);
console.log("Is num1 equal to num2?", isEqualTo);
console.log("Is num1 not equal to num2?", isNotEqual);
console.log("Is num1 less than or equal to num2?", isLessThanOrEqualTo);
console.log("Is num1 greater than or equal to num2?", isGreaterThanOrEqualTo);
